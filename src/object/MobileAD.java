package object;

public class MobileAD {
	private String Title,OpenUrl,ThumbImg;
	private int  ID;
	
	public MobileAD(String title, String openUrl, String thumbImg, int iD) {
		super();
		Title = title;
		OpenUrl = openUrl;
		ThumbImg = thumbImg;
		ID = iD;
	}
	public String getTitle() {
		return Title;
	}
	public void setTitle(String title) {
		Title = title;
	}
	public String getOpenUrl() {
		return OpenUrl;
	}
	public void setOpenUrl(String openUrl) {
		OpenUrl = openUrl;
	}
	public String getThumbImg() {
		return ThumbImg;
	}
	public void setThumbImg(String thumbImg) {
		ThumbImg = thumbImg;
	}
	public int getID() {
		return ID;
	}
	public void setID(int iD) {
		ID = iD;
	}
	
}
